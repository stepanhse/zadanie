filename = open("zadanietext.txt", "r")
number = filename.read()
ints = number.split(" ")
ints = list(map(int, ints))
print("максимальное число это : ", max(ints))
print("минимальное число это : ", min(ints))
sum = 0
for x in ints:
    sum += x
print("сумма цифр числа равна: ", sum)
mult = 1
for y in ints:
    mult *= y
print("произведение цифр равно: ", mult)
import time
start_time = time.time()
print("--- %s seconds ---" % (time.time() - start_time))
